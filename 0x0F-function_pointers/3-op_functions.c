#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: int
 * @b: int
 * Return: a + b.
 */

int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - the substraction of a and b.
 * @a: int
 * @b: int
 * Return: a - b.
 */

int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - the production of a and b.
 * @a: int
 * @b: int
 * Return: a * b.
 */

int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - the division of a by b.
 * @a: int
 * @b: int
 * Return: a / b.
 */

int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - the remaining of the division of a by b.
 * @a: int
 * @b: int
 * Return: a % b.
 */

int op_mod(int a, int b)
{
    return (a % b);
}